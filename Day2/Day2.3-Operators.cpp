# include <iostream>
using namespace std;

int main()
{

             // ARITHMETIC
             cout<<"ARITHMETIC"<<endl;
cout<<4+5<<endl;
cout<<4-5<<endl;
cout<<4*5<<endl;
cout<<5/3<<endl;
cout<<5%3<<endl;

             // RELATIONAL
             cout<<"RELATIONAL"<<endl;
cout<<(4>5)<<endl;
cout<<(4<5)<<endl;
cout<<(4>=5)<<endl;
cout<<(4<=5)<<endl;
cout<<(4==5)<<endl;
cout<<(4!=5)<<endl;

             // LOGICAL
             cout<<"LOGICAL"<<endl;
             cout<<(4>5 && 5>6)<<endl;
             cout<<(4>5 || 5>6)<<endl;
             cout<<(!(4>5))<<endl;

             // BITWISE
}